    public void opPendente (){
        if(operacao != "nada"){
            if(operacao == "soma"){
                valor2 = Double.parseDouble(TxtRes.getText());
                valor3 = valor2+valor1;
                valor1 = valor3;
                TxtRes.setText(String.valueOf(valor1));
            }
            if(operacao == "sub"){
                valor3 = valor2;
                valor1 = valor1-valor2;
                valor2 = 0.0;
            }
            if(operacao == "mul"){
                valor3 = valor2;
                valor1 = valor1*valor2;
                valor2 = 0.0;
            }
            if(operacao == "div"){
                valor3 = valor2;
                valor1 = valor1/valor2;
                valor2 = 0.0;
            }
            if(operacao == "exp"){
                valor1 = Math.pow(valor1, 2);
            }
            if(operacao == "raiz"){
                valor1 = Math.sqrt(valor1);
            }
        }
        
    }

